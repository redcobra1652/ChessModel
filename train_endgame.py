def gen_kpk(n):
    """KPK: the side to move has K+P, opponent has only K.
    Labelled as theoretical 'win' -- OUTCOME_WEIGHTS will handle the drawn
    cases (e.g. blocked rook pawn) via the play-out result."""
    positions = []
    attempts = 0
    while len(positions) < n and attempts < n * 20:
        attempts += 1
        pawn_file = random.randint(0, 7)
        pawn_rank = random.randint(1, 5)  # ranks 1-5 (0-indexed), not 0 or 6/7
        wp = chess.square(pawn_file, pawn_rank)
        wk = _random_square(exclude=[wp])
        bk = _random_square(exclude=[wp, wk])
        if _kings_too_close(wk, bk, min_dist=2):
            continue
        board = _make_board(
            (wk, chess.Piece(chess.KING, chess.WHITE)),
            (bk, chess.Piece(chess.KING, chess.BLACK)),
            (wp, chess.Piece(chess.PAWN, chess.WHITE)),
        )
        if board is None:
            continue
        for b, t in _both_colors(board, "win"):
            positions.append((b, t))
            if len(positions) >= n:
                break
    return positions[:n]


