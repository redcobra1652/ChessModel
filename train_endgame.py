#!/usr/bin/env python3
"""
train_endgame.py -- Supervised endgame training pass for the AlphaZero chess network.

Why this exists: the main stockfish_train.py loop generates games at 1320 Elo, which
rarely reaches clean endgames. The value head therefore gets almost no signal for
"king + queen vs king = won" vs "drawn position", causing the model to fail to convert
winning endgames and occasionally lose drawn ones.

KEY DESIGN PRINCIPLE -- color balance:
  Every endgame type is generated in ALL FOUR color/turn combinations:
    1. White-up, White-to-move   (theoretical: "win")
    2. Black-up, Black-to-move   (theoretical: "win")
    3. White-up, Black-to-move   (theoretical: "loss" -- defender to move)
    4. Black-up, White-to-move   (theoretical: "loss" -- defender to move)

  Cases 1 & 2 are produced by board.mirror(). Cases 3 & 4 flip the turn on
  each board so the material-DOWN side moves first. This is critical: without
  cases 3 & 4, every game starts with the attacker moving, defender plies
  always produce z=-1, and the value head collapses to always predicting -1.

  The theoretical outcome label is always expressed from the perspective of the
  SIDE TO MOVE at the start of the position:
    "win"  = the side to move should win with best play
    "draw" = the position is a theoretical draw with best play
    "loss" = the side to move should lose with best play (defender-to-move cases)
  This means z targets will be naturally balanced (+1 / 0 / -1) rather than
  skewed toward any one color.

Bonus/penalty weight hierarchy (applied to loss scaling, not value targets):
  - Lose a winning endgame:  -1.0  (heaviest -- this is the core bug to fix)
  - Draw a winning endgame:  -0.5  (moderate -- failed to convert a won position)
  - Win a winning endgame:   +0.8  (strong -- reinforce correct conversion)
  - Lose a drawn endgame:    -0.3  (light -- shouldn't lose but opponent didn't blunder)
  - Draw a drawn endgame:     0.0  (correct outcome -- no extra signal needed)
  - Win a drawn endgame:     +0.15 (very light -- opponent blundered, don't overfit)

Sign convention: value targets z are ALWAYS from the side-to-move's perspective,
matching train.py and stockfish_train.py exactly. Do not change this.

Usage:
    python3 train_endgame.py --model best_model.pt --stockfish-dir stockfish \\
        --device mps --positions 1000 --train-steps 50

    # Re-run whenever check_value_head.py shows regression
"""

