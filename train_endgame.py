def gen_drawn_positions(n):
    """Theoretically drawn positions: KBK, KNK, KBKB same color.
    These teach the model to recognize draws and not play for a win."""
    positions = []
    attempts = 0
    generators = [_gen_kbk, _gen_knk, _gen_kbkb_same_color]
    while len(positions) < n and attempts < n * 20:
        attempts += 1
        result = random.choice(generators)()
        if result is not None:
            board, theoretical = result
            # Mirror half of drawn positions too for color balance
            if random.random() < 0.5:
                board = board.mirror()
            positions.append((board, theoretical))
    return positions[:n]


def _gen_kbk():
    wk = _random_square()
    bk = _random_square(exclude=[wk])
    if _kings_too_close(wk, bk, min_dist=2):
        return None
    wb = _random_square(exclude=[wk, bk])
    board = _make_board(
        (wk, chess.Piece(chess.KING, chess.WHITE)),
        (bk, chess.Piece(chess.KING, chess.BLACK)),
        (wb, chess.Piece(chess.BISHOP, chess.WHITE)),
    )
    return (board, "draw") if board else None


def _gen_knk():
    wk = _random_square()
    bk = _random_square(exclude=[wk])
    if _kings_too_close(wk, bk, min_dist=2):
        return None
    wn = _random_square(exclude=[wk, bk])
    board = _make_board(
        (wk, chess.Piece(chess.KING, chess.WHITE)),
        (bk, chess.Piece(chess.KING, chess.BLACK)),
        (wn, chess.Piece(chess.KNIGHT, chess.WHITE)),
    )
    return (board, "draw") if board else None


def _gen_kbkb_same_color():
    wk = _random_square()
    bk = _random_square(exclude=[wk])
    if _kings_too_close(wk, bk, min_dist=2):
        return None
    wb = _random_square(exclude=[wk, bk])
    wb_color = (chess.square_rank(wb) + chess.square_file(wb)) % 2
    candidates = [sq for sq in range(64)
                  if sq not in [wk, bk, wb]
                  and (chess.square_rank(sq) + chess.square_file(sq)) % 2 == wb_color]
    if not candidates:
        return None
    bb = random.choice(candidates)
    board = _make_board(
        (wk, chess.Piece(chess.KING, chess.WHITE)),
        (bk, chess.Piece(chess.KING, chess.BLACK)),
        (wb, chess.Piece(chess.BISHOP, chess.WHITE)),
        (bb, chess.Piece(chess.BISHOP, chess.BLACK)),
    )
    return (board, "draw") if board else None


# ---------------------------------------------------------------------------
# Stockfish binary detection
# ---------------------------------------------------------------------------

def _is_executable(path):
    if not os.path.isfile(path):
        return False
    st = os.stat(path)
    return bool(st.st_mode & (stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH))


def _candidate_score(filename):
    name = filename.lower()
    score = 0
    machine = platform.machine().lower()
    is_apple = "apple-silicon" in name or "m1" in name or "arm64" in name
    is_x86 = "x86-64" in name or "x86_64" in name
    if machine in ("arm64", "aarch64"):
        score += 100 if is_apple else (-50 if is_x86 else 0)
    else:
        score += 100 if is_x86 else (-100 if is_apple else 0)
    for feature, bonus in [("avx512", 40), ("vnni", 35), ("bmi2", 30),
                            ("avx2", 25), ("sse41-popcnt", 10), ("sse41", 5)]:
        if feature in name:
            score += bonus
            break
    if name == "stockfish":
        score += 15
    if name.endswith((".nnue", ".zip", ".tar")):
        score -= 1000
    return score


def find_stockfish_binary(stockfish_dir=None, explicit_path=None):
    if explicit_path:
        if not _is_executable(explicit_path):
            raise FileNotFoundError(f"--stockfish-path '{explicit_path}' not executable.")
        return explicit_path
    search_dir = stockfish_dir or "stockfish"
    if not os.path.isdir(search_dir):
        raise FileNotFoundError(f"Stockfish directory '{search_dir}' not found.")
    candidates = [p for p in glob.glob(os.path.join(search_dir, "**", "stockfish*"),
                                        recursive=True) if _is_executable(p)]
    if not candidates:
        raise FileNotFoundError(f"No executable stockfish binary under '{search_dir}'.")
    candidates.sort(key=_candidate_score, reverse=True)
    log.info(f"Auto-detected Stockfish binary: {candidates[0]}")
    return candidates[0]


# ---------------------------------------------------------------------------
# Play out one endgame position
# ---------------------------------------------------------------------------

def play_endgame(board_start, theoretical_outcome, sf_teacher, sf_player,
                 teacher_limit, player_limit, max_moves=100):
    """Play out a single endgame position with Stockfish on both sides.

    theoretical_outcome: "win" or "draw" from the SIDE TO MOVE's perspective
                         at the START of the position. This is the critical
                         difference from the old code which used White's perspective.

    Returns:
        examples: list of (state, policy_target, z, outcome_weight)
        actual_outcome: "win"/"draw"/"loss" from the STARTING side-to-move's perspective
    """
    board = board_start.copy()
    starting_color = board.turn  # remember who started -- outcome measured from their view
    raw_examples = []
    ply = 0

    while not board.is_game_over(claim_draw=True) and ply < max_moves:
        side_to_move = board.turn

        try:
            info = sf_teacher.analyse(board, teacher_limit)
            if not info.get("pv"):
                break
            teacher_move = info["pv"][0]
        except Exception as e:
            log.warning(f"Teacher analysis failed: {e}")
            break

        mirror = (side_to_move == chess.BLACK)
        policy_target = np.zeros(train.ACTION_SIZE, dtype=np.float32)
        policy_target[train.move_policy_index(teacher_move, mirror)] = 1.0
        state = train.board_to_tensor(board)
        raw_examples.append((state, policy_target, side_to_move))

        try:
            result = sf_player.play(board, player_limit)
            move = result.move
        except Exception as e:
            log.warning(f"Player move failed: {e}")
            break

        if move not in board.legal_moves:
            log.warning(f"Illegal move {move} at {board.fen()}")
            break
        board.push(move)
        ply += 1

    # Determine actual outcome from WHITE's perspective first
    if board.is_checkmate():
        winner_is_white = (board.turn == chess.BLACK)
        actual_z_white = 1.0 if winner_is_white else -1.0
    else:
        actual_z_white = 0.0  # draw or move limit

    # Convert to starting-side-to-move's perspective
    if starting_color == chess.WHITE:
        actual_z_stm = actual_z_white
    else:
        actual_z_stm = -actual_z_white

    if actual_z_stm > 0.5:
        actual_outcome = "win"
    elif actual_z_stm < -0.5:
        actual_outcome = "loss"
    else:
        actual_outcome = "draw"

    outcome_weight = OUTCOME_WEIGHTS.get((theoretical_outcome, actual_outcome), 0.0)

    # Build training examples -- z always from the PLY's side-to-move perspective
    examples = []
    for state, policy_target, side_to_move in raw_examples:
        z = actual_z_white if side_to_move == chess.WHITE else -actual_z_white
        z = float(np.clip(z, -1.0, 1.0))
        examples.append((state, policy_target, z, outcome_weight))

    return examples, actual_outcome


# ---------------------------------------------------------------------------
# Training step
# ---------------------------------------------------------------------------

def endgame_train_step(model, optimizer, batch, device,
                        value_loss_weight=2.0, label_smoothing=0.05):
    """One gradient step on a batch of endgame examples.

    Loss weight = BASE_WEIGHT + abs(outcome_weight).
    Penalty examples get high weight on a correctly-signed z target (already
    negative for losses), so the gradient pushes the value head in the right
    direction WITHOUT flipping the target.
    """
    states, policy_targets, zs, outcome_weights = zip(*batch)

    states_t  = torch.from_numpy(np.stack(states)).float().to(device)
    policy_t  = torch.from_numpy(np.stack(policy_targets)).float().to(device)
    z_t       = torch.tensor(zs, dtype=torch.float32).to(device)
    weights_t = torch.tensor([BASE_WEIGHT + abs(w) for w in outcome_weights],
                              dtype=torch.float32).to(device)
    weights_t = weights_t / (weights_t.mean() + 1e-8)

    logits, value = model(states_t)

    n_actions = logits.shape[1]
    smooth_policy = policy_t * (1.0 - label_smoothing) + label_smoothing / n_actions
    log_probs = F.log_softmax(logits, dim=1)
    policy_loss = (weights_t * -(smooth_policy * log_probs).sum(dim=1)).mean()

    value_loss = (weights_t * (value.squeeze(1) - z_t) ** 2).mean()

    loss = policy_loss + value_loss_weight * value_loss
    optimizer.zero_grad()
    loss.backward()
    torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
    optimizer.step()

    return policy_loss.item(), value_loss.item()


