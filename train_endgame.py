# ---------------------------------------------------------------------------
# Outcome weights.
# theoretical: "win" = side-to-move should win, "draw" = theoretical draw.
# actual:      "win" / "draw" / "loss" from side-to-move's perspective.
# ---------------------------------------------------------------------------
OUTCOME_WEIGHTS = {
    ("win",  "win"):   0.8,   # converted a winning endgame -- strong positive
    ("win",  "draw"): -0.5,   # failed to convert a won position -- moderate penalty
    ("win",  "loss"): -1.0,   # lost a winning endgame -- heaviest penalty (core bug)
    ("draw", "win"):   0.15,  # won a drawn endgame -- opponent blundered, tiny bonus
    ("draw", "draw"):  0.0,   # correctly held the draw -- neutral
    ("draw", "loss"): -0.3,   # lost a drawn endgame -- light penalty
    # Defender-to-move positions (generated by _both_colors cases 3 & 4):
    # theoretical="loss" means the side to move is the material-DOWN defender.
    ("loss", "loss"):  0.8,   # correctly lost as the defender -- strong positive
    ("loss", "draw"):  0.3,   # held a draw as the defender -- mild positive (stubborn defence)
    ("loss", "win"):  -0.5,   # won as the defender -- attacker blundered badly
}

BASE_WEIGHT = 0.3  # minimum loss weight so neutral examples still contribute


# ---------------------------------------------------------------------------
# Position generation helpers
# ---------------------------------------------------------------------------

def _random_square(exclude=None):
    exclude = set(exclude or [])
    sq = random.randint(0, 63)
    while sq in exclude:
        sq = random.randint(0, 63)
    return sq


def _kings_too_close(sq1, sq2, min_dist=2):
    r1, c1 = divmod(sq1, 8)
    r2, c2 = divmod(sq2, 8)
    return max(abs(r1 - r2), abs(c1 - c2)) < min_dist


def _make_board(*pieces):
    """
    Build a board from a list of (square, Piece) tuples.
    Always sets White to move, clears castling and en passant.
    Returns None if the position is invalid, checkmate, or stalemate.
    """
    board = chess.Board(fen=None)
    for sq, piece in pieces:
        board.set_piece_at(sq, piece)
    board.turn = chess.WHITE
    board.castling_rights = chess.BB_EMPTY
    board.ep_square = None
    if not board.is_valid():
        return None
    if board.is_checkmate() or board.is_stalemate():
        return None
    return board


def _both_colors(board, theoretical_stm):
    """
    Given a White-up, White-to-move board, return FOUR (board, theoretical_from_stm)
    pairs covering all combinations of which color holds the material advantage and
    which color is to move:

      1. White-up, White-to-move  (original)           theoretical = theoretical_stm ("win")
      2. Black-up, Black-to-move  (mirror of original)  theoretical = theoretical_stm ("win")
      3. White-up, Black-to-move  (original, turn flipped) theoretical = "loss"
         (Black is to move but White has the material -- Black should lose)
      4. Black-up, White-to-move  (mirror, turn flipped)   theoretical = "loss"
         (White is to move but Black has the material -- White should lose)

    Cases 3 & 4 were missing before: every generated game started with the
    material-up side to move, so defender plies always produced z=-1, flooding
    the value head with "mover loses" signal and causing collapse to -1.

    Note: flipping the turn on an already-built board is safe here because
    _make_board has already validated legality with White to move. We re-check
    for immediate checkmate/stalemate after flipping.
    """
    results = []

    # Cases 1 & 2: material-up side to move (original behaviour)
    mirrored = board.mirror()
    results.append((board, theoretical_stm))
    results.append((mirrored, theoretical_stm))

    # Cases 3 & 4: defender to move -- flip turn on each board
    for b in (board, mirrored):
        b2 = b.copy()
        b2.turn = not b2.turn  # give the move to the material-DOWN side
        if b2.is_valid() and not b2.is_checkmate() and not b2.is_stalemate():
            # theoretical from the new side-to-move's perspective: they are losing
            results.append((b2, "loss"))

    return results


# ---------------------------------------------------------------------------
# Endgame position generators
# Each generator produces n positions total across all four combinations:
#   attacker-to-move (White-up/White, Black-up/Black) and
#   defender-to-move (White-up/Black, Black-up/White).
# theoretical outcome is ALWAYS from the side-to-move's perspective.
# ---------------------------------------------------------------------------

def gen_kqk(n):
    """KQK: the side to move has K+Q, opponent has only K. Theoretical win."""
    positions = []
    attempts = 0
    while len(positions) < n and attempts < n * 20:
        attempts += 1
        wk = _random_square()
        bk = _random_square(exclude=[wk])
        if _kings_too_close(wk, bk, min_dist=2):
            continue
        wq = _random_square(exclude=[wk, bk])
        board = _make_board(
            (wk, chess.Piece(chess.KING, chess.WHITE)),
            (bk, chess.Piece(chess.KING, chess.BLACK)),
            (wq, chess.Piece(chess.QUEEN, chess.WHITE)),
        )
        if board is None:
            continue
        for b, t in _both_colors(board, "win"):
            positions.append((b, t))
            if len(positions) >= n:
                break
    return positions[:n]


